// increment/decrement operators

#include <stdio.h>

int main()
{
    int a = 30;
    int b = 15;
    float c = 10.5;
    float d = 62.8;
    
    printf ("using a++ on a: %d", a++);
    printf ("\nusing ++a on b: %d", ++b);
    printf ("\nusing a-- on c: %f", c--);
    printf ("\nusing --a on d: %f", --d);
    return 0;
}
